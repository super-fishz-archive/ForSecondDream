package team.gtfm.server.bean;

public class Coord {
	private int ChickenRowNum;
	private double lat;
	private double lng;
	
	public int getChickenRowNum() {
		return ChickenRowNum;
	}
	public void setChickenRowNum(int chickenRowNum) {
		ChickenRowNum = chickenRowNum;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double lng) {
		this.lng = lng;
	}
	
	
}
